package but2.s4.festiplandroid.festivals;

public class Scene {
    private int idScene;

    private String nomScene;

    private String tailleScene;

    private int spectateurMax;

    private String coordonneesGPS;

    private int idFestival;

    public int getIdScene() {
        return idScene;
    }

    public void setIdScene(int idScene) {
        this.idScene = idScene;
    }

    public String getNomScene() {
        return nomScene;
    }

    public void setNomScene(String nomScene) {
        this.nomScene = nomScene;
    }

    public String getTailleScene() {
        return tailleScene;
    }

    public void setTailleScene(String tailleScene) {
        this.tailleScene = tailleScene;
    }

    public int getSpectateurMax() {
        return spectateurMax;
    }

    public void setSpectateurMax(int spectateurMax) {
        this.spectateurMax = spectateurMax;
    }

    public String getCoordonneesGPS() {
        return coordonneesGPS;
    }

    public void setCoordonneesGPS(String coordonneesGPS) {
        this.coordonneesGPS = coordonneesGPS;
    }

    public int getIdFestival() {
        return idFestival;
    }

    public void setIdFestival(int idFestival) {
        this.idFestival = idFestival;
    }
}
